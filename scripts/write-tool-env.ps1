# tool-verify.sh — Bash smoke check. Use a single-quoted here-string
# to avoid PowerShell interpolating the $(...) Bash subshells.
$verifyShBody = @'
#!/usr/bin/env bash
set -e
BASE_URL="__BASE_URL__"
API_KEY="__API_KEY__"
echo "[tool-verify] GET $BASE_URL/v1/models"
models_status=$(curl -sS -H "Authorization: Bearer $API_KEY" -o /tmp/openrelay-models.json -w "%{http_code}" "$BASE_URL/v1/models" || true)
if [ "$models_status" != "200" ]; then
  echo "[tool-verify] /v1/models failed (HTTP $models_status). Is the relay running? Try: npm.cmd run start" >&2
  exit 1
fi
echo "[tool-verify] /v1/models ok"
echo "[tool-verify] POST $BASE_URL/v1/chat/completions (model = auto)"
chat_status=$(curl -sS -X POST -H "Authorization: Bearer $API_KEY" -H "Content-Type: application/json" \
  -d '{"model":"auto","messages":[{"role":"user","content":"reply with the single word: pong"}]}' \
  -o /tmp/openrelay-chat.json -w "%{http_code}" "$BASE_URL/v1/chat/completions" || true)
if [ "$chat_status" != "200" ]; then
  echo "[tool-verify] /v1/chat/completions failed (HTTP $chat_status)" >&2
  exit 2
fi
echo "[tool-verify] /v1/chat/completions ok"
echo "[tool-verify] OK — Codex, OpenClaw, OpenCode, Aider, Goose, Continue, Claude Code, Amp should all be able to talk to the relay through $BASE_URL."
'@
$verifyShBody = $verifyShBody.Replace("__BASE_URL__", $baseUrl).Replace("__API_KEY__", $apiKey)
