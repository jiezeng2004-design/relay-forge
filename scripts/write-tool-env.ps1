$verifyCmdBody = @'
@echo off
setlocal
set BASE_URL=__BASE_URL__
set API_KEY=__API_KEY__
echo [tool-verify] GET %BASE_URL%/v1/models
curl.exe -sS -H "Authorization: Bearer %API_KEY%" "%BASE_URL%/v1/models" -o "%TEMP%\openrelay-models.json" -w "HTTP %%{http_code}%%"
if errorlevel 1 (
  echo [tool-verify] /v1/models failed. Is the relay running? Try: npm.cmd run start
  exit /b 1
