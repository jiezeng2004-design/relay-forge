$ErrorActionPreference = "Stop"

  <#
  OpenRelay Local Safe — tool onboarding script generator.

  Writes project-internal helper scripts that local AI tools (Codex,
  OpenClaw, OpenCode, Aider, Goose, Continue, Claude Code, Amp) can source
  to point at the running relay. The scripts only set environment variables
  for the current shell process. They do NOT touch global system env
  variables, the Windows Registry, or any token / cookie / session
  storage.

  Generated files (live next to this script, i.e. in the project root):
    tool-env.ps1      - dot-source from Windows PowerShell
    tool-env.cmd      - call from Windows CMD
    tool-env.sh       - source from WSL / Linux / macOS Bash
    tool-verify.ps1   - run a smoke /v1/models + /v1/chat/completions check
    tool-verify.cmd   - same for CMD
    tool-verify.sh    - same for WSL / Linux / macOS Bash

  Usage (from the project root, in Windows PowerShell):

      powershell -ExecutionPolicy Bypass -File scripts\write-tool-env.ps1
      . .\tool-env.ps1
      .\tool-verify.ps1
#>

$root = Split-Path -Parent $PSScriptRoot
$psOutput = Join-Path $root "tool-env.ps1"
$cmdOutput = Join-Path $root "tool-env.cmd"
$shOutput = Join-Path $root "tool-env.sh"
$verifyPsOutput = Join-Path $root "tool-verify.ps1"
$verifyCmdOutput = Join-Path $root "tool-verify.cmd"
$verifyShOutput = Join-Path $root "tool-verify.sh"
$port = if ($env:PORT) { $env:PORT } else { "18765" }
$baseUrl = "http://127.0.0.1:$port/v1"
$apiKey = if ($env:RELAY_TOKEN) { $env:RELAY_TOKEN } else { "local" }

# Shared env-var block covering all 7 supported tools. Tools that do
# not understand a given var just ignore it; sourcing the script in a
# shell where the tool is launched is enough.
$envVars = @(
  "OPENAI_BASE_URL",
  "OPENAI_API_BASE",
  "OPENAI_API_KEY",
  "ANTHROPIC_BASE_URL",
  "ANTHROPIC_API_KEY"
)

# .ps1 — PowerShell
