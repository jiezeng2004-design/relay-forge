[void]$psBody.AppendLine("# Generated by RelayForge.")
[void]$psBody.AppendLine("# Run in the SAME PowerShell window that will start the tool:")
[void]$psBody.AppendLine("#   . .\tool-env.ps1")
[void]$psBody.AppendLine("# This only sets process-local env vars; it does NOT modify")
[void]$psBody.AppendLine("# system / user environment or any tool config file.")
foreach ($name in $envVars) {
