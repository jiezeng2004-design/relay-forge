  [void]$psBody.AppendLine("`$env:$name = `"$value`"")
}
[void]$psBody.AppendLine("")
[void]$psBody.AppendLine("# Per-tool aliases the tools look at:")
[void]$psBody.AppendLine("`$env:CODEX_BASE_URL = `"$baseUrl`"")
[void]$psBody.AppendLine("`$env:CODEX_API_KEY = `"$apiKey`"")
[void]$psBody.AppendLine("`$env:OPENCLAW_BASE_URL = `"$baseUrl`"")
[void]$psBody.AppendLine("`$env:OPENCLAW_API_KEY = `"$apiKey`"")
[void]$psBody.AppendLine("`$env:OPENCODE_BASE_URL = `"$baseUrl`"")
[void]$psBody.AppendLine("`$env:OPENCODE_API_KEY = `"$apiKey`"")
[void]$psBody.AppendLine("`$env:AIDER_OPENAI_API_BASE = `"$baseUrl`"")
[void]$psBody.AppendLine("`$env:AIDER_OPENAI_API_KEY = `"$apiKey`"")
[void]$psBody.AppendLine("`$env:GOOSE_OPENAI_BASE_URL = `"$baseUrl`"")
[void]$psBody.AppendLine("`$env:GOOSE_OPENAI_API_KEY = `"$apiKey`"")
[void]$psBody.AppendLine("`$env:CONTINUE_OPENAI_BASE_URL = `"$baseUrl`"")
[void]$psBody.AppendLine("`$env:CONTINUE_OPENAI_API_KEY = `"$apiKey`"")
