echo [tool-verify] /v1/models ok
echo [tool-verify] POST %BASE_URL%/v1/chat/completions (model = auto)
curl.exe -sS -X POST -H "Authorization: Bearer %API_KEY%" -H "Content-Type: application/json" -d "{\"model\":\"auto\",\"messages\":[{\"role\":\"user\",\"content\":\"reply with the single word: pong\"}]}" "%BASE_URL%/v1/chat/completions" -o "%TEMP%\openrelay-chat.json" -w "HTTP %%{http_code}%%"
if errorlevel 1 (
  echo [tool-verify] /v1/chat/completions failed.
  exit /b 2
)
echo [tool-verify] /v1/chat/completions ok
echo [tool-verify] OK — Codex, OpenClaw, OpenCode, Aider, Goose, Continue, Claude Code, Amp should all be able to talk to the relay through %BASE_URL%.
'@
