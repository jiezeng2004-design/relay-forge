# tool-verify.ps1 — PowerShell smoke check
$verifyPsBody = @'
$ErrorActionPreference = "Stop"
$baseUrl = "__BASE_URL__"
$apiKey = "__API_KEY__"
Write-Host "[tool-verify] GET $baseUrl/v1/models"
try {
  $models = Invoke-RestMethod -Uri "$baseUrl/v1/models" -Headers @{ Authorization = "Bearer $apiKey" } -TimeoutSec 5
  Write-Host ("[tool-verify] /v1/models ok: " + $models.data.Count + " models")
} catch {
  Write-Host "[tool-verify] /v1/models failed: $_" -ForegroundColor Red
  Write-Host "[tool-verify] Is the relay running? Try: npm.cmd run start" -ForegroundColor Yellow
  exit 1
}
Write-Host "[tool-verify] POST $baseUrl/v1/chat/completions (model = auto)"
$body = @{ model = "auto"; messages = @(@{ role = "user"; content = "reply with the single word: pong" }) } | ConvertTo-Json -Depth 10 -Compress
try {
  $chat = Invoke-RestMethod -Uri "$baseUrl/v1/chat/completions" -Method Post -ContentType "application/json" -Headers @{ Authorization = "Bearer $apiKey" } -Body $body -TimeoutSec 30
  Write-Host "[tool-verify] /v1/chat/completions ok: model=$($chat.model)"
} catch {
  Write-Host "[tool-verify] /v1/chat/completions failed: $_" -ForegroundColor Red
  exit 2
}
Write-Host "[tool-verify] OK — Codex, OpenClaw, OpenCode, Aider, Goose, Continue, Claude Code, Amp should all be able to talk to the relay through $baseUrl."
'@
$verifyPsBody = $verifyPsBody.Replace("__BASE_URL__", $baseUrl).Replace("__API_KEY__", $apiKey)
[System.IO.File]::WriteAllText($verifyPsOutput, $verifyPsBody.ToString(), [System.Text.Encoding]::UTF8)

